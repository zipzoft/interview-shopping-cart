<?php

namespace ZipzoftInterview\ShoppingCart\Entity;

/**
 * ProductEntity
 * Tells the shopping cart how to store and retrieve products
 */
class ProductEntity
{
    /**
     * ID of the product
     */
    public $id;

    /**
     * Name of the product
     */
    public $name;

    /**
     * Price of the product
     */
    public $price;

    /**
     * Quantity of the product
     */
    public $description;

    /**
     * Quantity of the product
     */
    public $quantity;
}